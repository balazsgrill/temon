package hu.temon.expressions;

/**
 * @author balazs.grill
 *
 */
public class ExpressionResolveException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = -3572008230926014237L;

	public ExpressionResolveException() {
		super();
	}

	public ExpressionResolveException(String message, Throwable cause,
			boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public ExpressionResolveException(String message, Throwable cause) {
		super(message, cause);
	}

	public ExpressionResolveException(String message) {
		super(message);
	}

	public ExpressionResolveException(Throwable cause) {
		super(cause);
	}
	
	

}
